/**
 *  quick_sort_tr: [[a]] -> [a] -> [a]
 */
var quick_sort_tr = function (source_list, accumulating) {

    if (accumulating == undefined)
        return quick_sort_tr([source_list], []);

    if (source_list.length == 0)
        return accumulating;

    var source = source_list[0];
    source_list = source_list.slice(1);

    if (source.length <= 1) {

        for (var i = 0; i < source.length; i++)
            accumulating.push(source[i]);
    }
    else {

        var pivot_index = Math.floor(source.length / 2) - 1;
        var pivot = source[pivot_index];
        var left = [],
            right = [];

        for (var i = 0; i < source.length; i++) {
            if (pivot_index == i)
                continue;
            if (source[i] < pivot)
                left.push(source[i]);
            else
                right.push(source[i]);
        }

        if (left.length <= 1) {
            for (var i = 0; i < left.length; i++)
                accumulating.push(left[i]);
            accumulating.push(pivot);
            source_list.unshift(right);
        }
        else {
            source_list.unshift(right);
            source_list.unshift([pivot]);
            source_list.unshift(left);
        }
    }

    console.log('quick_sort_tr called');
    return quick_sort_tr(source_list, accumulating);
};

